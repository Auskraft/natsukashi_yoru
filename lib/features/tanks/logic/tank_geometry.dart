import 'dart:math';

/// Геометрия игрового поля «Танчиков» в целочисленной суб-клеточной решётке.
///
/// Классический Battle City: поле 13×13 тайлов, танк 2×2 тайла, движение по
/// полу-тайлу, разрушение кирпича по четвертям. Чтобы симуляция была
/// детерминированной (нужно для режима «Дейли» и для тестов) и без накопления
/// ошибки округления, все позиции танков и терреина считаем в целых
/// **суб-клетках** — 8 на тайл. Рендер делит на [field] → нормализованные [0,1].
class TankGeo {
  const TankGeo._();

  /// Тайлов по стороне поля.
  static const int tiles = 13;

  /// Суб-клеток в одном тайле.
  static const int sub = 8;

  /// Суб-клеток по стороне поля (13 × 8 = 104).
  static const int field = tiles * sub;

  /// Сторона танка в суб-клетках (2 тайла = 16).
  static const int tankSize = 2 * sub;

  /// Полу-тайл в суб-клетках (= сторона квадранта кирпича). Шаг выравнивания.
  static const int half = sub ~/ 2;

  /// Максимальная координата верхне-левого угла танка включительно (104−16 = 88).
  static const int maxOrigin = field - tankSize;

  /// Снап координаты к ближайшему полу-тайлу (кратному [half]). Применяется при
  /// повороте, чтобы танк ровно входил в проёмы и не залипал в углах.
  static int alignToHalf(int v) {
    final r = v % half;
    if (r == 0) return v;
    return (r * 2 >= half) ? v - r + half : v - r;
  }

  /// Нормализовать суб-координату в долю поля [0, 1].
  static double norm(num subCoord) => subCoord / field;

  /// Центр тайла в нормализованных координатах (для попапов/бонусов/базы).
  static Point<double> tileCenterNorm(int tx, int ty) =>
      Point((tx * sub + sub / 2) / field, (ty * sub + sub / 2) / field);
}
